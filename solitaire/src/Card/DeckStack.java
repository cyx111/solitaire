package Card;
//等待取用的纸牌
public class DeckStack extends CardStack {
	public void push(CardStack pCStack, int index) {
	if(pCStack != null)
	{
	    for(int i = index; i >= 0; --i) {
	    	this.getPokers_card().add(pCStack.peek(i));
	    }
    }
    pCStack.peek().setFaceUp(false);
    //添加牌时需设置背面朝上
    }
}
